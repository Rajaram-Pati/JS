const key = 3

switch (key) {
    case 1:
        console.log("key is 1");
        
        break;
    case 2:
        console.log("key is 2");
        
        break;
    case 3:
        console.log("key is 3");
        
        break;
    case 4:
        console.log("key is 4");
        
        break;

    default:
        console.log("key is not 1, 2, 3, or 4");
        
        break;
        
}